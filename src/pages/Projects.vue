                      <p>
                        There are 3 types of heat transfer, conduction, convection, and radiation. The method of heat transfer which is used in our project is radiation. Radiation does not rely on contact to transfer heat like conduction and convection. Radiation uses infrared light which has longer wavelengths than regular light to transfer energy. An example of radiation is your microwave or the sun. Our project uses mostly radiation. The infrared light from our fire and fuel is the main way that the food is cooked. But the smoke (which uses the method of convection for heat transfer) also cooks the food through convection (through the air) and the metal plates heat the food through conduction (touch), which allows for a more evenly cooked meal in shorter cook time.
