                      <h2 class="subtitle has-text-black">Our Process</h2>
                      <p>
                        For our project, we built a wood-fired oven. First, we used a yoga ball and cut a hole slightly smaller than it in a wooden board and fit the board into it. Then we used tape to attach half of a metal barrel to the ball and covered the whole thing with tape. We left enough room for a yogurt cup for an exhaust pipe. We made a mixture of cement, per tile, and water for the actual form of the dome. We completely covered it in the mixture and let it dry for a few days until the cast is complete and ready. Next is to deflate the ball and remove the tape from the inside as the cast has now dried. The table our oven will be on will be made of various wooden panels and planks. Lay down fire bricks on the table and make sure there are as small of gaps as possible. Put the cast on top of the bricks and cover the dome completely in fire blanket material and then cover that in chicken wire. Then add on another layer of the mixture from earlier on top of the fire blanket and chicken wire and wait a few days to dry. Make a fire and start small, work your way up to a big fire, make modifications, and add things if needed. You now have your firewood oven!
                      </p>
